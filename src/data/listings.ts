// Demo helper: each listing gets 4 images (primary + 3 from the bank), so the
// in-card slider has something to scroll through until real per-listing photos
// are imported. Replace with real per-listing arrays in production.
const IMAGE_BANK = [IMG.m1, IMG.m2, IMG.m3, IMG.m4, IMG.apt];
const IMMEUBLE_BANK = [IMG.im1, IMG.im2, IMG.im3];

for (const l of listings) {
  if (l.images && l.images.length > 0) continue;
  const bank = l.type === "Immeuble de rapport" ? IMMEUBLE_BANK : IMAGE_BANK;
  const seed = [...l.id].reduce((a, c) => a + c.charCodeAt(0), 0);
  const others = bank.filter((img) => img !== l.image);
  const start = seed % others.length;
  l.images = [
    l.image,
    others[start],
    others[(start + 1) % others.length],
    others[(start + 2) % others.length],
  ];
}

